package operators;

public class RelationalOperators {
    public static void main(String[] args) {

        int num1 = 12;
        int num2 = 7;
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1==num2);

        boolean result =num1!=num2;
        System.out.println(result);
        result =num1>=num2;
        System.out.println(result);
        result =num1<=num2;
        System.out.println(result);


    }
}
